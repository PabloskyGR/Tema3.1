package ejercicio1;

public class Suma {
	
	static int suma(int num1, int num2) {
		int sumaTotal;
		
		sumaTotal = num1 + num2;
		
		return sumaTotal;
	}
	
	static double suma(double num1, double num2) {
		double sumaTotal;
		
		sumaTotal = num1 + num2;
		
		return sumaTotal;
		
	}

}
