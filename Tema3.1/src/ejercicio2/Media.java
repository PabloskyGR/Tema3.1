package ejercicio2;

public class Media {
	
	static double media(int num1, int num2) {
		double media;
		
		media = (num1 + num2) / 2;
			
		return media;
	}
	
	static double media(int num1, int num2, int num3) {
		double media;
		
		media = (num1 + num2 + num3) / 3;
		
		return media;
	}

}
